package com.example.discoverbuc.Common.HelperClasses;

public class IconCategoryHelperClass {

    int icon;
    String title;

    public IconCategoryHelperClass(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }
}
